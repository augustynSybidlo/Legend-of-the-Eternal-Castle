def create_board_from_string( line_length=87, string=string_map):
    board = []
    line = []
    count = 0
    while len(board) < 19:
        for letter in string:
            if letter == "\n":
                continue
            line.append(letter)
            count += 1
            if count % line_length == 0:
                board.append(line)
                line = []
    for line in board:
        print("".join(line))
    return board
